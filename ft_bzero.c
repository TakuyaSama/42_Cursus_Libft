#include <strings.h>

void	bzero(void *s, size_t n)
{
	int i;

	if (n == 0)
		return (0);
	else
	{
		i = 0;
		while (i < n)
		{
			s[i] = 0;
		}
		s[i] = '\0';
	}
}
